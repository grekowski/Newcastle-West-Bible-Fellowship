			<h2 class="about">About</h2>
			<h3>We are simply a group of people whose lives have been changed by the Lord Jesus Christ. We began meeting together as a local church in 1999. It is our desire to be a Christ-centred church with our members growing in their understanding of Christ, growing in their relationship to Christ, and growing in Christ like character.</h3>
			<p>We believe the Bible to be the Word of God and therefore sufficient for all matters of faith and practice. We are not part of any denomination, yet we are thankful for the fellowship we enjoy with many other Christians here in Co. Limerick, around Ireland, and throughout the world.</p>
			
			<h5 class='about'>Mission</h5>
			<div>
				<p>We believe that it is our responsibility, as believers in the Lord Jesus, to share the good news about who He is with the whole world.</p>
				<p>We endeavour to model our personal and church life on the teaching of God's Word rather than on personal opinions or religious traditions, and so seek to follow the priorities established in the early church, including the apostles' doctrine, fellowship, breaking of bread, and prayer.</p>
