			<h5 class='about'>Beliefs</h5>
			<div>
				<p><span class="about" style="font-weight: bold;">Beliefs</span> - We reach out to all people, no matter who they are, what they've done or where they've been. Rather than run from culture, we’ve chosen to engage it to connect with people and show them God's timeless truths. We do not belong to any "organisation" or "denomination". We are not a sect or Jehovah's Witnesses. We believe that each church, or assembly, is a fully independent body. There are similar churches in the area, throughout the country and worldwide but each is fully independent, shepherded by a group of elders who are responsible only to God for this activity.</p>
				<p><span class="about" style="font-weight: bold;">The Scriptures</span> - The Bible is inspired by God and is the final authority on all spiritual matters. "All scripture is given by inspiration of God, and is profitable for doctrine, for reproof, for correction, for instruction in righteousness" (2 Timothy 3:16). The Scriptures are clear, "making wise the simple" (Psalm 19:7). The Scriptures are sufficient for faith and practice, and "that the man of God may be complete, thoroughly equipped for every good work" (2 Timothy 3:17).</p>
				<p><span class="about" style="font-weight: bold;">Salvation</span> - We believe salvation involves the redemption of the whole man, and is offered freely to all who receive Jesus Christ as Lord and Savior, who by His death and resurrection obtained eternal redemption for the believer. This salvation was required because, in common with all mankind, we are sinners in the sight of God (Romans chapter 3 verse 23) and can do nothing of ourselves to merit God's favour. However Jesus Christ, the Eternal Son of the Eternal God, became a man, lived a perfect, sinless life and gave His life on the cross of Calvary to pay the penalty for our sin. It is through repentance from our sin and by faith in Jesus Christ that we have salvation and fellowship again with God. "For this is good and acceptable in the sight of God our Saviour, who desires all men to be saved and to come to the knowledge of the truth" (1 Timothy 2:3-4).</p>
				<p><span class="about" style="font-weight: bold;">God</span> - We believe God is the eternal, uncreated Creator and Sustainer of all things (Genesis 1:1; Colossians 1:16–17). He is the First and the Last, and beside Him there is no god (Isaiah 44:6). This one God eternally existent in three persons, Father, Son & Holy Spirit. The Lord Jesus Christ alone is the Head of the church. He is the only mediator between God and men. (1 Timothy 2:5). </p>
				<p><span class="about" style="font-weight: bold;">Jesus Christ</span> - We believe Jesus Christ is fully God and fully man, that He came in the flesh and was born of a virgin, and lived a sinless life (John 8:46, 18:38b, 19:4, 21:25). He taught that He is the only means of our salvation, by which man would be reconciled to God (Colossians 2:9, Acts 4:12). </p>
				<p><span class="about" style="font-weight: bold;">Gift</span> - We believe eternal life is a gift from God and is received by grace through faith apart from any merit (Romans 6:23). No believer is justified by works of the Law (Ephesians 2:8-9).</p>
				<p><span class="about" style="font-weight: bold;">The Lord's Supper</span> - We believe the Lord's Supper was instituted by Christ on the night He was betrayed (1 Corinthians 11:23). The Lord's Supper consists of bread and wine, which represent His body that was broken for believers and His blood that was shed for believers (1 Corinthians 11:24–25). The Lord's Supper serves as a reminder to both believers and non-believers that Jesus has died, is risen, and is coming again (1 Corinthians 11:26). The Bible instructs us to meet together as believers for the remembering of the Lord and His death, prayers and Bible teaching (Acts 2:42). </p>
